c="Welcome to my profile"
print(c.isalnum())#true if string made of A-Z,a-z,0-9
print(c.isalpha())#same as isalnumexcept 0-9
print(c.islower())
print(c.isprintable())#if \n is present then returns false
print(c.isspace()) #returns true if white space is present
print(c.istitle())#returns true if every word is start with capital letter
print(c.isupper())
print(c.swapcase())
print(c.title())






